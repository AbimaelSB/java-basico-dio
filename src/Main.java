public class Main {
  public static void main(String[] args) {
    int ano = 2023; //variaveis em camelCase
    final boolean SIM = true; //variaveis imutaveis posuem a palavra chave 'final' e são escritas em maiusculo
    final boolean NAO = false; //variaveis imutaveis posuem a palavra chave 'final' e são escritas em maiusculo
    System.out.printf("%s %d%s\n", "Happy New Year!!! Welcome", ano, "!!!");
    System.out.println(SIM);
    System.out.println(NAO);
  }
}
